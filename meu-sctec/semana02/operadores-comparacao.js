//Operadores de comparacao com IF / ELSE
let idade = 10

console.log(idade == "19") //comparação de igualdade -> true ou false
console.log(idade ==="19") //comparção de igualdade estrita -> true ou false 
console.log(idade!=19) //comparação de diferença -> true ou false
console.log(idade!==19) //comparação de diferença estrita -> true ou false
console.log(idade>18) //comparação de maior que -> true ou false
console.log(idade<18) //comparação de menor que -> true ou false
console.log(idade>=18) //comparação de maior ou igual a -> true ou false
console.log(idade<=18) //comparação de menor ou igual a -> true ou false

